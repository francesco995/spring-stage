package com.objectway.stage.model;

import java.io.Serializable;

@SuppressWarnings("serial")
abstract class AbstractModelObject implements Serializable {
	private String codice;
	private String descrizione;
	
	public AbstractModelObject(String codice, String descrizione) {
		super();
		this.codice = codice;
		this.descrizione = descrizione;
	}
	public String getCodice() {
		return codice;
	}
	public void setCodice(String codice) {
		this.codice = codice;
	}
	public String getDescrizione() {
		return descrizione;
	}
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}
	@Override
	public String toString() {
		return getClass().getSimpleName() + " [codice=" + codice + ", descrizione=" + descrizione + "]";
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((codice == null) ? 0 : codice.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractModelObject other = (AbstractModelObject) obj;
		if (codice == null) {
			if (other.codice != null)
				return false;
		} else if (!codice.equals(other.codice))
			return false;
		return true;
	}
}
